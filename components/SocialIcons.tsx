/**
 * Get brand colors for social platforms
 */
export const getSocialBrandColor = (label: string): string => {
  const normalizedLabel = label.toLowerCase().replace(/\s+/g, '');
  
  switch (normalizedLabel) {
    case 'github':
      return '#333';
    case 'linkedin':
      return '#0077B5';
    case 'x':
    case 'twitter':
      return '#000000';
    case 'instagram':
      return '#E4405F';
    case 'dribbble':
      return '#EA4C89';
    case 'behance':
      return '#1769FF';
    case 'codepen':
      return '#000000';
    case 'youtube':
      return '#FF0000';
    case 'discord':
      return '#5865F2';
    default:
      return '#666666';
  }
};

